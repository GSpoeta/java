package Lista4;

public class quatro  {

    public static void main(String[] args) {

        // aqui faz um loop começando em 9 e indo ate 1 diminuindo de 2 em 2
        for (int i = 9; i >= 1; i = i - 2) {
            
            // aqui mostra o valor atual do i
            System.out.println(i);
        }

        // aqui o programa termina
    }
}