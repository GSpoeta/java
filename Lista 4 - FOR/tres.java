package Lista4;

public class tres {

    public static void main(String[] args) {

        // aqui cria uma variavel pra guardar a soma dos numeros
        int soma = 0;

        // aqui faz um loop de 1 ate 10
        for (int i = 1; i <= 10; i++) {
            
            // aqui vai somando o valor de i na variavel soma
            soma = soma + i;
        }

        // aqui mostra o resultado final da soma
        System.out.println("Soma = " + soma);

        // aqui o programa termina
    }
}