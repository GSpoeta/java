package Lista5FA;

public class balao {
    
    public static void main(String[] args) {
        
        // aqui mostra o titulo do programa
        System.out.println("\n=== Balão ===");
        
        // aqui inicia o tamanho do balao em 0
        int tamanho = 0;
        
        // aqui vai aumentando o tamanho do balao ate chegar em 10
        while (tamanho <= 10) {
            
            // aqui mostra o tamanho atual do balao
            System.out.println("Balão tamanho: " + tamanho);
            
            // aqui aumenta o tamanho em 1 a cada repeticao
            tamanho++;
        
        }
    }    
}